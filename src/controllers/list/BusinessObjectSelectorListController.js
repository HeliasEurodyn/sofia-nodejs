const asyncHandler = require('../../helpers/asyncHandler');
const BusinessObjectSelectorListService = require('../../services/list/BusinessObjectSelectorListService');

module.exports = {

   getList: asyncHandler(async (req, res) => {
      const results = await BusinessObjectSelectorListService.getList({
         data: req.body,
         ctx: {
            userId: req.user?.id || ''
            }
      });

      res.json(results);
   }),

   getSqlgf2List: asyncHandler(async (req, res) => {
      const results = await BusinessObjectSelectorListService.getSqlgf2List({
         data: req.body,
         ctx: {
            userId: req.user?.id || ''
            }
      });

      res.json(results);
   }),

   getSqlgf3List: asyncHandler(async (req, res) => {
      const results = await BusinessObjectSelectorListService.getSqlgf3List({
         data: req.body,
         ctx: {
            userId: req.user?.id || ''
            }
      });

      res.json(results);
   })

};