const asyncHandler = require('../../helpers/asyncHandler');
const VocabularyKeywordsFormService = require('../../services/form/VocabularyKeywordsFormService');

module.exports = {

   getById: asyncHandler(async (req, res) => {
      const results = await VocabularyKeywordsFormService.getById({
         id: req.params.id,
         ctx: {
            userId: req.user?.id || ''
            }
      });

      res.json(results);
   }),

   create: asyncHandler(async (req, res) => {
      const results = await VocabularyKeywordsFormService.create({
         data: req.body,
         ctx: {
            userId: req.user?.id || ''
            }
      });

      res.json(results);
   }),

   update: asyncHandler(async (req, res) => {
      const results = await VocabularyKeywordsFormService.update({
         data: req.body,
         ctx: {
            userId: req.user?.id || ''
            }
      });

      res.json(results);
   }),

   remove: asyncHandler(async (req, res) => {
      const results = await VocabularyKeywordsFormService.remove({
         id: req.params.id,
         ctx: {
            userId: req.user?.id || ''
            }
      });

      res.json(results);
   }),


};