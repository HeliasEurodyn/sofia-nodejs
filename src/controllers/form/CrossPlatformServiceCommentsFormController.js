const asyncHandler = require('../../helpers/asyncHandler');
const CrossPlatformServiceCommentsFormService = require('../../services/form/CrossPlatformServiceCommentsFormService');

module.exports = {

   getById: asyncHandler(async (req, res) => {
      const results = await CrossPlatformServiceCommentsFormService.getById({
         id: req.params.id,
         ctx: {
            userId: req.user?.id || ''
            }
      });

      res.json(results);
   }),

   create: asyncHandler(async (req, res) => {
      const results = await CrossPlatformServiceCommentsFormService.create({
         data: req.body,
         ctx: {
            userId: req.user?.id || ''
            }
      });

      res.json(results);
   }),

   update: asyncHandler(async (req, res) => {
      const results = await CrossPlatformServiceCommentsFormService.update({
         data: req.body,
         ctx: {
            userId: req.user?.id || ''
            }
      });

      res.json(results);
   }),

   remove: asyncHandler(async (req, res) => {
      const results = await CrossPlatformServiceCommentsFormService.remove({
         id: req.params.id,
         ctx: {
            userId: req.user?.id || ''
            }
      });

      res.json(results);
   }),


};