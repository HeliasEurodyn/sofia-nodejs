const asyncHandler = require('../../helpers/asyncHandler');
const DataCatalogueServiceFormService = require('../../services/form/DataCatalogueServiceFormService');

module.exports = {

   getById: asyncHandler(async (req, res) => {
      const results = await DataCatalogueServiceFormService.getById({
         id: req.params.id,
         ctx: {
            userId: req.user?.id || ''
            }
      });

      res.json(results);
   }),

   create: asyncHandler(async (req, res) => {
      const results = await DataCatalogueServiceFormService.create({
         data: req.body,
         ctx: {
            userId: req.user?.id || ''
            }
      });

      res.json(results);
   }),

   update: asyncHandler(async (req, res) => {
      const results = await DataCatalogueServiceFormService.update({
         data: req.body,
         ctx: {
            userId: req.user?.id || ''
            }
      });

      res.json(results);
   }),

   remove: asyncHandler(async (req, res) => {
      const results = await DataCatalogueServiceFormService.remove({
         id: req.params.id,
         ctx: {
            userId: req.user?.id || ''
            }
      });

      res.json(results);
   }),


};