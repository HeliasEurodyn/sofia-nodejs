const asyncHandler = require('../../helpers/asyncHandler');
const ScenarioCypherQueryFormService = require('../../services/form/ScenarioCypherQueryFormService');

module.exports = {

   getById: asyncHandler(async (req, res) => {
      const results = await ScenarioCypherQueryFormService.getById({
         id: req.params.id,
         ctx: {
            userId: req.user?.id || ''
            }
      });

      res.json(results);
   }),

   create: asyncHandler(async (req, res) => {
      const results = await ScenarioCypherQueryFormService.create({
         data: req.body,
         ctx: {
            userId: req.user?.id || ''
            }
      });

      res.json(results);
   }),

   update: asyncHandler(async (req, res) => {
      const results = await ScenarioCypherQueryFormService.update({
         data: req.body,
         ctx: {
            userId: req.user?.id || ''
            }
      });

      res.json(results);
   }),

   remove: asyncHandler(async (req, res) => {
      const results = await ScenarioCypherQueryFormService.remove({
         id: req.params.id,
         ctx: {
            userId: req.user?.id || ''
            }
      });

      res.json(results);
   }),


};