const asyncHandler = require('../../helpers/asyncHandler');
const CypherQueryTemplateFormService = require('../../services/form/CypherQueryTemplateFormService');

module.exports = {

   getById: asyncHandler(async (req, res) => {
      const results = await CypherQueryTemplateFormService.getById({
         id: req.params.id,
         ctx: {
            userId: req.user?.id || ''
            }
      });

      res.json(results);
   }),

   create: asyncHandler(async (req, res) => {
      const results = await CypherQueryTemplateFormService.create({
         data: req.body,
         ctx: {
            userId: req.user?.id || ''
            }
      });

      res.json(results);
   }),

   update: asyncHandler(async (req, res) => {
      const results = await CypherQueryTemplateFormService.update({
         data: req.body,
         ctx: {
            userId: req.user?.id || ''
            }
      });

      res.json(results);
   }),

   remove: asyncHandler(async (req, res) => {
      const results = await CypherQueryTemplateFormService.remove({
         id: req.params.id,
         ctx: {
            userId: req.user?.id || ''
            }
      });

      res.json(results);
   }),


};