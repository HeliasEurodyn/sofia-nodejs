const asyncHandler = require('../../helpers/asyncHandler');
const CypherScenarioQueryFormService = require('../../services/form/CypherScenarioQueryFormService');

module.exports = {

   getById: asyncHandler(async (req, res) => {
      const results = await CypherScenarioQueryFormService.getById({
         id: req.params.id,
         ctx: {
            userId: req.user?.id || ''
            }
      });

      res.json(results);
   }),

   create: asyncHandler(async (req, res) => {
      const results = await CypherScenarioQueryFormService.create({
         data: req.body,
         ctx: {
            userId: req.user?.id || ''
            }
      });

      res.json(results);
   }),

   update: asyncHandler(async (req, res) => {
      const results = await CypherScenarioQueryFormService.update({
         data: req.body,
         ctx: {
            userId: req.user?.id || ''
            }
      });

      res.json(results);
   }),

   remove: asyncHandler(async (req, res) => {
      const results = await CypherScenarioQueryFormService.remove({
         id: req.params.id,
         ctx: {
            userId: req.user?.id || ''
            }
      });

      res.json(results);
   }),


};