const asyncHandler = require('../../helpers/asyncHandler');
const CloudConnectorSettingsFormService = require('../../services/form/CloudConnectorSettingsFormService');

module.exports = {

   getById: asyncHandler(async (req, res) => {
      const results = await CloudConnectorSettingsFormService.getById({
         id: req.params.id,
         ctx: {
            userId: req.user?.id || ''
            }
      });

      res.json(results);
   }),

   create: asyncHandler(async (req, res) => {
      const results = await CloudConnectorSettingsFormService.create({
         data: req.body,
         ctx: {
            userId: req.user?.id || ''
            }
      });

      res.json(results);
   }),

   update: asyncHandler(async (req, res) => {
      const results = await CloudConnectorSettingsFormService.update({
         data: req.body,
         ctx: {
            userId: req.user?.id || ''
            }
      });

      res.json(results);
   }),

   remove: asyncHandler(async (req, res) => {
      const results = await CloudConnectorSettingsFormService.remove({
         id: req.params.id,
         ctx: {
            userId: req.user?.id || ''
            }
      });

      res.json(results);
   }),


};